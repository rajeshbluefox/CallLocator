    private EditText et_search;
    private List<NumLocatorModel> models = new ArrayList<>();
    private RecyclerView recyclerView;
    private ISDNumberAdapter mAdapter;

    private ImageView btBack;
    @Override
    public void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_isd_code);
//        getWindow().setFlags(WindowManager.LayoutParams.FLAG_LAYOUT_NO_LIMITS, WindowManager.LayoutParams.FLAG_LAYOUT_NO_LIMITS);


        for (int i = 0; i < coutryName.length; i++) {
            NumLocatorModel mModel = new NumLocatorModel(coutryName[i], code[i]);
            models.add(mModel);
        }

//        RelativeLayout rel_banner = findViewById(R.id.rel_banner);
//        loadBannerAds(rel_banner);

        RelativeLayout rl_adplaceholder = findViewById(R.id.rl_adplaceholder);
        MyApplication.getInstance().loadNativeAd(rl_adplaceholder, ISDCodeActivity.this);


        recyclerView = findViewById(R.id.recyclerView);
        recyclerView.setLayoutManager(new LinearLayoutManager(this));
        recyclerView.setHasFixedSize(true);
        mAdapter=new ISDNumberAdapter(this,models);
        recyclerView.setAdapter(mAdapter);

        et_search = findViewById(R.id.et_search);
        et_search.addTextChangedListener(new TextWatcher() {
            @Override
            public void beforeTextChanged(CharSequence s, int start, int count, int after) {

            }

            @Override
            public void onTextChanged(CharSequence s, int start, int before, int count) {
                searchFilter(s.toString());
            }

            @Override
            public void afterTextChanged(Editable s) {

            }
        });

        btBack = findViewById(R.id.btBack);
        btBack.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View v) {
                finish();
            }
        });
    }

    private void searchFilter(String text) {
        List<NumLocatorModel> temp = new ArrayList();
        for (NumLocatorModel model : models) {
            if (model.getCoutryName().toLowerCase().contains(text)) {
                temp.add(model);
            }
        }
        mAdapter.updateList(temp);
    }

